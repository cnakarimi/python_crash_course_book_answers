def print_messages(messages):
    for message in messages:
        print(message)


messages = ['LOL – laughing out loud.',
            'OMG – oh my god (or oh my gosh)',
            'IMO – in my opinion.',
            'IMHO – in my humble opinion (or in my honest opinion)',
            'BTW – by the way.',
            "IDK – I don't know.",
            'LMK – let me know.']

print_messages(messages)
